public class datatypes{
 
    public static void main(String[] args) {
        /*
        Datatype - datatype is a type of data which can be understand by the computer
        1) primitive 2) non primitive
        primitive- int, char, float, double, longint, shortint, boolean
        nonprimitive - string, array, class, interface
        */
       float var = 3;
       System.out.println("float=" + var); //4 byte
       double var2 = 3.83;
       System.out.println("double=" + var2);// 8byte
       char var3 = 'a';// string- sequence of characters.
       System.out.println("char=" + var3);
       boolean var4 = true;
       System.out.println("boolean=" + var4);
       short var5 = 5000;
       System.out.println("short=" + var5);
       long var6 = 500000000;
       System.out.println("long=" + var6);
       String var7 = "Hi guys how are you?";
       System.out.println("string=" + var7);



            
   
       







        
    }
   
   
}
